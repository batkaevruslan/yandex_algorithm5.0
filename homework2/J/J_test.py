import J
import pytest

@pytest.mark.parametrize("board, expected", [
    ([
        [".", "#", "#"],
        ["#", "#", "#"],
        [".", "#", "#"]
     ],
     ("YES",
      [
        [".", "a", "a"],
        ["b", "a", "a"],
        [".", "a", "a"]
     ])),
     #2
     ([
        ["#"],
        ["."]
     ],
     ("NO", None)),
    #3
     ([
        [".", "."],
        ["#", "#"]
     ],
     ("YES",
      [
        [".", "."],
        ["a", "b"]
     ])),
     #4
     ([
        ["#", "#", "#"]
     ],
     ("YES",
      [
        ["a", "b", "b"]
     ])),
     #5
     ([
        ["#", "#", "#", "#", "."]
     ],
     ("YES",
      [
        ["a", "b", "b", "b", "."]
     ])),
     #6
     ([
        ["#", ".", "#"],
        [".", "#", "."],
        [".", ".", "."]
     ],
     ("NO", None)),
     #7
     ([
        ["#", ".", "#", "#"],
        ["#", ".", ".", "."]
     ],
     ("YES",
      [
        ["a", ".", "b", "b"],
        ["a", ".", ".", "."]
     ])),
     #8
     ([
        [".", ".", ".", "."],
        ["#", "#", ".", "."],
        ["#", "#", "#", "."],
        [".", "#", "#", "."],
     ],
     ("NO", None)),
     #9
     ([
        [".", "#", ".", "."],
        ["#", "#", "#", "."]
     ],
     ("YES", [
        [".", "b", ".", "."],
        ["a", "a", "a", "."]
     ]))

])
def test_indicatesWheetherTwoRectangles(board, expected):
    result = J.getResult(board)
    assert result[0] == expected[0]
    assert result[1] == expected[1]